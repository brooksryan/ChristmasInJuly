			<p>Seven years ago to this very day, a group of plucky young ne’erdowells each had the same dream: they were standing on a stage, surrounded by adoring fans, performing amateur improv and sketch comedy. Each dream was linked by a strange occurrance: it was definitely Christmas, but it may also have been July. But it was probably at least near Christmas. 
			</p>
			<p>
			This dream perplexed the friends for almost a decade, during which time they did nothing to further their goals of becoming actors and writers.
			</p>
			<p>
			But last week, they had the dream again, and they knew it was time. Time to pull themselves up by their bootstraps, brush off the dust, and turn that dream into a reality. 
			</p>
			<p>
			Christmas in July (Sometime around Christmas) is the best representation of those dreams we could manufacture. While we weren’t able to get Bill Clinton to volunteer as an usher and we also don’t live in a world where trees and dolphins one time mated a lot and now we live in a diverse society of humans and treedolphins, we came pretty close to the original dream. 
			</p>
			<p>
			</p>
			<p>
			If you’re interested, sign up for a ticket. It’s free, because we’re not sure we’re worth actual money art should be free to everyone. 
			</p>
			<p>
			If you’d like to participate, you can submit your work or audition for improv up until November 15th. 
